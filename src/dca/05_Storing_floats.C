#include<stdio.h>

void main() 
{
  // Storing 12.67 into variable f, 34.89 into variable f2, 23.56 into variable f3
  float f = 12.67;
  float f2 = 34.89;
  float f3 = 23.56;
    
  // Printing values stored into flaot varaibles f, f2, f3
  printf("f = %f \n", f);
  printf("f2 = %f \n", f2);
  printf("f3 = %f", f3);
}

